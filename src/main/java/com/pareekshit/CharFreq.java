package main.java.com.pareekshit;

public class CharFreq {
    Character c;
    Integer freq;

    public CharFreq() {
    }

    public CharFreq(Character c, Integer freq) {
        this.c = c;
        this.freq = freq;
    }

    public Character getC() {
        return c;
    }

    public void setC(Character c) {
        this.c = c;
    }

    public Integer getFreq() {
        return freq;
    }

    public void setFreq(Integer freq) {
        this.freq = freq;
    }
}
